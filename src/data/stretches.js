/**
 * Get stretches by muscle group
 * @param {string|string[]} muscleGroups - Single muscle group or array of muscle groups
 * @returns {Array} Array of stretches targeting those muscle groups
 */
export const getStretchesByMuscle = (muscleGroups) => {
  const groups = Array.isArray(muscleGroups) ? muscleGroups : [muscleGroups];
  return stretchesData.filter(stretch => 
    groups.some(group => 
      stretch.primaryMuscles.includes(group) || 
      stretch.secondaryMuscles.includes(group)
    )
  );
};

/**
 * Get all unique muscle groups from stretches
 * @returns {Array} Array of unique muscle group names
 */
export const getStretchMuscleGroups = () => {
  const groups = new Set();
  stretchesData.forEach(stretch => {
    stretch.primaryMuscles.split(',').forEach(m => groups.add(m.trim()));
    if (stretch.secondaryMuscles) {
      stretch.secondaryMuscles.split(',').forEach(m => {
        const muscle = m.trim();
        if (muscle) groups.add(muscle);
      });
    }
  });
  return Array.from(groups).sort();
};
